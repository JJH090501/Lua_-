-- 오늘은 지렁이 게이ㅁ을 만들어볼거에여
-- 루아는 원래 게이ㅁ 만들기에 최적화 된 코드에요 ( + 임베딩 )

-- 우선 프레임의 높이와 너비를 정해줄게여. 프레임은 맵을 뜻한답니다
local width, height = 20, 10 

-- 이제 이 프레임 안에 지렁이 게이ㅁ을 구현해볼게여
local snake = "@" -- 지렁이게이 는 '@' 문자로 표시할게여
local snakeX, snakeY = math.floor(width / 2), math.floor(height / 2) -- 지렁이의 초기 위치를 프레임의 중앙으로  math.floor는 내림 함수에여


for y = 1, height do -- y축을 따라서 프레임을 만들어줄게여. 높이 먼저 반복해야 하는 이유는 y축이 세로 방향이기 때문이에여 반대로 해도 상관없긴한데 그럼 값이 서로 바껴서 나와여
    for x = 1, width do -- x축을 따라서 프레임을 만들어줄게여
        if x == 1 or x == width or y == 1 or y == height then -- 맵 경계 부분을 만들어줄게여. 맨 첫번째 부분과 끝 부분만 .으로 나타낸답니다
            io.write(".") -- 프레임의 경계 부분을 '#' 문자로 표시
        elseif x == snakeX and y == snakeY then
            io.write(snake)  -- 지렁이 위치
        else
            io.write(" ") -- 프레임 내부는 공백으로 표시
        end
    end
    io.write("\n") -- 각 행이 끝날 때마다 줄 바꿈
end
-- 이렇게 하면 20x10 크기의 프레임이 만들어져여

-- 지렁이의 위치를 프레임 중앙에 표시
print(string.format("지렁이 위치: (%d, %d)", snakeX, snakeY))

-- 이제 지렁이 게이ㅁ의 기본적인 프레임이 완성되었어여
-- 다음 단계로는 지렁이를 움직이게 만들거나 먹이를 추가하는 등의 기능을 구현할 수 있답니다
